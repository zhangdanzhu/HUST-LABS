;子程序名：F10T2
;功能：将SI指向的有符号十进制字符串转换成二进制数
;入口参数：
;EAX/AX――存放待转换的二进制数
;DX――32位或16位
;出口参数：
;打印在屏幕上
;所使用的寄存器：
;EBX――
;SI――存放转换后字符串地址

NAME	F2T10
EXTRN	RADIX:NEAR
PUBLIC	F2T10

.386
DATA	SEGMENT	USE16	PUBLIC
BUF	DB	12 DUP(?)
DATA	ENDS
CODE	SEGMENT	USE16	PUBLIC
	ASSUME	CS:CODE, DS:DATA
F2T10	PROC		
	PUSH	EBX
	PUSH	SI
	LEA	SI, BUF
	CMP	DX, 32
	JE	B
	MOVSX	EAX, AX
B:	OR	EAX, EAX
	JNS	PLUS
	NEG	EAX
	MOV	BYTE PTR [SI], '-'
	INC	SI
PLUS:	MOV	EBX, 10
	CALL	RADIX
	MOV	BYTE PTR [SI], '$'
	LEA	DX, BUF
	MOV	AH, 9
	INT	21H
	POP	SI
	POP	EBX
	RET
F2T10	ENDP
CODE	ENDS
	END